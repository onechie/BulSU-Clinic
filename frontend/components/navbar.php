<script>
    let animationInProgress = false;
    const menuToggler = () => {
        if (animationInProgress) {
            return; // Do nothing if an animation is already in progress
        }

        animationInProgress = true; // Set the flag to true to indicate an animation is starting

        const menuContainer = document.getElementById('menu-container');
        const menuLabels = document.getElementsByClassName('menu-label');

        if (menuContainer.classList.contains('expanded')) {
            menuContainer.classList.remove('expanded');

            for (const menuLabel of menuLabels) {
                menuLabel.classList.add('hidden');
                anime({
                    targets: menuLabel,
                    opacity: 0,
                    easing: "easeOutExpo", // Easing function
                    duration: 100 // Animation duration in milliseconds
                })
            }

            anime({
                targets: menuContainer,
                width: "50px", // Set the desired width
                easing: "easeOutExpo", // Easing function
                duration: 500, // Animation duration in milliseconds
                complete: function() {
                    animationInProgress = false; // Set the flag to false to indicate the animation is complete
                }
            });
        } else {
            menuContainer.classList.add('expanded');
            anime({
                targets: menuContainer,
                width: "200px", // Set the desired width
                easing: "easeOutExpo", // Easing function
                duration: 300, // Animation duration in milliseconds
                complete: () => {
                    for (const menuLabel of menuLabels) {
                        menuLabel.classList.remove('hidden');
                        anime({
                            targets: menuLabel,
                            opacity: 1,
                            easing: "easeOutExpo", // Easing function
                            duration: 500 // Animation duration in milliseconds
                        })
                    }
                    animationInProgress = false; // Set the flag to false to indicate the animation is complete
                }
            });

        }

    }
</script>